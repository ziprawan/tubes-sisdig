char data_length(char width)
{
  char p = width * HEIGHT;
  if (p % 8 != 0)
  {
    p += 8;
    p -= p % 8;
  }
  p /= 8;

  return p;
}

short find_letter(char letter)
{
  for (unsigned char i = 0; i < sizeof(font_data);)
  {
    if (font_data[i] == letter)
    {
      return i + 1;
    }
    i += 2 + data_length(font_data[i + 1]);
  }

  return -1;
}

char letter_width(char letter)
{
  short idx = find_letter(letter);
  if (idx == -1)
    return -1;

  return font_data[idx];
}

char letter_data_at_column(char letter, char column)
{
  short idx = find_letter(letter);
  if (idx == -1)
    return 0;

  char width = font_data[idx++];
  if (column >= width)
    return 0;

  char result = 0; // Bit dari atas ke bawah

  char bit = 0;
  for (char i = 0; i < HEIGHT; i++)
  {
    bit += column;
    if (bit >= 8)
    {
      bit %= 8;
      idx++;
    }

    result <<= 1;
    result |= (font_data[idx] >> (7 - bit)) & 1;

    bit += (width - column);
  }

  return result;
}